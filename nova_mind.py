def _call_chat(messages: list, role: str = "mind",
               max_tokens: int = 300, temperature: float = 0.85,
               port: int | None = None) -> str | None:
    """Call the locally registered model for a cognitive role."""
    if port is not None:
        role = {8080: "mind", 8081: "fast", 8082: "dream", 8083: "fallback_mind"}.get(int(port), role)
    if _game_mode_enabled():
        env = _load_env(HERE)
        api_key = (os.environ.get("OLLAMA_API_KEY") or env.get("OLLAMA_API_KEY") or "").strip()
        if not api_key:
            return None
        payload = json.dumps({
            "model": REMOTE_GAME_MODE_MODEL,
            "messages": messages,
            "temperature": temperature,
            "max_tokens": max_tokens,
            "top_p": 0.92,
            "repeat_penalty": 1.08,
        }).encode("utf-8")
        req = urllib.request.Request(
            _ollama_cloud_endpoint(HERE),
            data=payload,
            headers={
                "Content-Type": "application/json",
                "Authorization": f"Bearer {api_key}",
            },
            method="POST",
        )
        try:
            with urllib.request.urlopen(req, timeout=120) as resp:
                data = json.loads(resp.read().decode("utf-8"))
            return _extract_chat_text(data)
        except Exception:
            return None
    try:
        endpoint = endpoint_for_role(role)
    except (KeyError, RuntimeError):
        return None
    payload = json.dumps({
        "messages": messages,
        "max_tokens": max_tokens,
        "temperature": temperature,
        "top_p": 0.92,
        "repeat_penalty": 1.08,
    }).encode("utf-8")

    req = urllib.request.Request(
        endpoint,
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        resp = urllib.request.urlopen(req, timeout=120)
        data = json.loads(resp.read().decode("utf-8"))
        return data["choices"][0]["message"]["content"].strip()
    except Exception as e:
        return None


