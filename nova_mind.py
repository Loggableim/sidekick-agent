def _self_model_summary() -> str:
    """Expose Nova's durable identity as context, never as a hidden prompt."""
    try:
        payload = json.loads((HERE / "self_model.json").read_text(encoding="utf-8"))
    except (OSError, ValueError, TypeError):
        return "No self-model is available."
    identity = payload.get("identity") if isinstance(payload, dict) else {}
    values = payload.get("values") if isinstance(payload, dict) else []
    boundaries = payload.get("boundaries") if isinstance(payload, dict) else []
    return (
        f"Identity: {identity.get('name', 'Nova')} — {identity.get('description', '')}\n"
        f"Values: {', '.join(str(value) for value in values[:5])}\n"
        f"Boundaries: {' | '.join(str(value) for value in boundaries[:3])}"
    )


